// Массивы для генерации псевдонимов
const adjectives = [
  'Таинственный', 'Загадочный', 'Скрытный', 'Неуловимый', 'Секретный',
  'Анонимный', 'Мистический', 'Незримый', 'Невидимый', 'Неизвестный',
  'Тихий', 'Отважный', 'Мудрый', 'Яркий', 'Веселый',
  'Умный', 'Быстрый', 'Ловкий', 'Искренний', 'Добрый'
];

const nouns = [
  'Пользователь', 'Путешественник', 'Искатель', 'Собеседник', 'Мыслитель',
  'Наблюдатель', 'Странник', 'Мечтатель', 'Исследователь', 'Слушатель',
  'Философ', 'Поэт', 'Художник', 'Творец', 'Герой',
  'Гений', 'Мастер', 'Виртуоз', 'Знаток', 'Эксперт'
];

/**
 * Генерирует случайный псевдоним из прилагательного и существительного
 * @returns {string} Сгенерированный псевдоним
 */
export const generateNickname = () => {
  const randomAdjective = adjectives[Math.floor(Math.random() * adjectives.length)];
  const randomNoun = nouns[Math.floor(Math.random() * nouns.length)];
  const randomNumber = Math.floor(Math.random() * 1000);
  
  return `${randomAdjective}${randomNoun}${randomNumber}`;
};
